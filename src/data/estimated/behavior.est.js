// ─────────────────────────────────────────────────────────────
// 消費性向（baseMPC）サンプリングパラメータ
// @derivation
//   総務省「家計調査」2022年の所得五分位別 平均消費性向
//   （STAT_AVERAGE_PROPENSITY_TO_CONSUME_BY_QUINTILE）を
//   年収水準と対応付けてフィッティング。
//   APC（平均消費性向）と MPC（限界消費性向）は理論上異なるが、
//   シミュレーターでは APC を baseMPC として扱い、
//   エージェントが「このターンに使える金額 × baseMPC」を消費するモデルを採用。
//   intercept と incomeElasticity は最小二乗法で推計。
// @status  推計値。所得弾力性の符号と大きさは家計調査と整合。
//          ただし個人ばらつき（noiseStdDev）は調整値。
// ─────────────────────────────────────────────────────────────
export const EST_MPC_PARAMS = {
  intercept:         0.950, // 最低所得水準での上限値（上界）
  incomeElasticity: -0.180, // 所得が10倍増えるごとに MPC がこれだけ低下
  referenceIncome:   1_000_000, // log の基点となる参照所得（100万円）
  noiseStdDev:       0.050, // 個人差ノイズ（調整値）
  min:               0.350,
  max:               0.980,
  // 計算式: baseMPC = clamp(
  //   intercept + incomeElasticity * log10(income / referenceIncome)
  //   + gauss(0, noiseStdDev), min, max)
  // 年収200万 → 約0.89、年収500万 → 約0.79、年収1000万 → 約0.74
};

